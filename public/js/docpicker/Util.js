    function formatPersonString(p) {
        var name_label = "", role_label = "";
        if(p.type === 'domain') {
            name_label = "all " + p.email + " domain users";                    
        }

        // default is the general public 
        else if(p.type === 'default') {
            name_label = "Public";
        }
        else {
            name_label = (p.first_name && p.last_name) ? (p.first_name + " " + p.last_name) : p.email;
            role_label = (p.class_role && dojo.trim(p.class_role).toLowerCase() != "student") ? " (" + p.class_role + ")" : "";            
        }

        // if the user's not in the roster, call them out as a guest
        if(!p.class_role) role_label = " (guest)";

        return name_label + role_label;
    }

    /*
     * Formats zulu date string
     * if current day, returns time string
     * else returns date string
     * @param {String} dateStr zulu date string
     */
    function formatDate( dateStr, dateObj ) {
        var dObj = dateStr.replace(/\D/g, ' ').split(' ');           
        var dateUTC = Date.UTC( dObj[0], (dObj[1]-1), dObj[2], dObj[3], dObj[4], dObj[5] );
        var dateUtcObj = new Date( dateUTC );
        
        if( Date.parse( dateObj ) - dateUTC >= getMillisecsToday() )
            return dojo.date.locale.format( dateUtcObj, {datePattern: 'MM/dd/yyyy', selector: 'date'});
        else
            return dojo.date.locale.format( dateUtcObj, {datePattern: 'HH:mma', selector: 'time'});
        
        function getMillisecsToday () {
            var now = new Date() ;      
            var hoursMs = now.getHours() * 3600000;
            var minsMs =  now.getMinutes() * 60000;
            var secsMs = now.getSeconds() * 1000;
            var ms = now.getMilliseconds();
            var milli = hoursMs + minsMs + secsMs + ms;
            return milli
        }
    }
    
